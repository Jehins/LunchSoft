CREATE TABLE EMPLEADO (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  USER VARCHAR(100) UNIQUE,
  NAME VARCHAR(100),
  AGE INT,
  TYPE_FK INT,
  PASS VARCHAR(100),
  FOREIGN KEY (TYPE_FK) REFERENCES TYPES(ID)
);

CREATE TABLE RESERVA (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  NUMERO INT,
  HORA DATETIME,
  EMPLEADO_FK INT,
  FOREIGN KEY (EMPLEADO_FK) REFERENCES EMPLEADO(ID)
);

CREATE TABLE PEDIDOS (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  DESCRIPCION VARCHAR (100),
  RESERVA_FK INT,
  FOREIGN KEY (RESERVA_FK) REFERENCES RESERVA(ID)
);

CREATE TABLE MENU (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  NAME VARCHAR(100),
  PRICE DECIMAL(10, 2),
  DESCRIPCION VARCHAR(100),
  IMAGEN BLOB
);

CREATE TABLE BEBIDA (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  NAME VARCHAR(100),
  PRICE DECIMAL(10, 2),
  DESCRIPCION VARCHAR(100),
  IMAGEN BLOB
);
CREATE TABLE PEDIDOS_DETALLES (
  ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
  DESCRIPCION VARCHAR(100),
  MENU_FK INT,
  PEDIDO_FK INT,
  BEBIDA_FK INT,
  FOREIGN KEY (MENU_FK) REFERENCES MENU(ID),
  FOREIGN KEY (BEBIDA_FK) REFERENCES BEBIDA(ID),
  FOREIGN KEY (PEDIDO_FK) REFERENCES PEDIDOS(ID)
);

CREATE TABLE FACTURA (
 ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
 VALOR DECIMAL,
 PEDIDOD_FK INT,
  FOREIGN KEY (PEDIDOD_FK) REFERENCES PEDIDOS_DETALLES(ID)
);